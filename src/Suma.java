
public class Suma {
    int n1, n2;

    public Suma(int n1, int n2) {
        this.n1 = n1;
        this.n2 = n2;
    }

    /**
     *
     * @param num1 primer parametro
     * @param num2 segundo parametro
     * @return resultado de la suma de los digitos
     */
    public int sumar(int num1, int num2){
        return num1 + num2;
    }

}
